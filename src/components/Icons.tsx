interface SortIconProps {
  className?: string;
  order: "asc" | "desc";
  type: "name" | "date" | "default";
}

export const SortIcon: React.FC<SortIconProps> = ({
  className,
  order,
  type,
}) => {
  const renderIcon = () => {
    switch (type) {
      case "name":
        return (
          <>
            <path d="M3 7h6" />
            <path d="M3 11h4" />
            <path d="M3 15h4" />
            <path d="M17 7v10" />
            <path d={order === "asc" ? "m14 14 3 3 3-3" : "m14 10 3-3 3 3"} />
          </>
        );
      case "date":
        return (
          <>
            <rect x="3" y="4" width="18" height="18" rx="2" ry="2" />
            <line x1="16" y1="2" x2="16" y2="6" />
            <line x1="8" y1="2" x2="8" y2="6" />
            <line x1="3" y1="10" x2="21" y2="10" />
            <path d={order === "asc" ? "m9 14 3 3 3-3" : "m9 16 3-3 3 3"} />
          </>
        );
      default:
        return (
          <>
            <path d="M12 20V4" />
            <path d={order === "asc" ? "m5 11 7-7 7 7" : "m5 13 7 7 7-7"} />
          </>
        );
    }
  };

  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      width="24"
      height="24"
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth="2"
      strokeLinecap="round"
      strokeLinejoin="round"
      className={className}
    >
      {renderIcon()}
    </svg>
  );
};

